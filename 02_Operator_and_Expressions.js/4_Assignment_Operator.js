//ASSIGNMENT OPERATOR
let m = 10; // Assigns the value 10 to the variable m
m += 5; // Equivalent to m = m + 5 (adds 5 to the current value of x)
console.log(m);
m -= 5; // Equivalent to m = m - 5 
console.log(m);
m *= 5; // Equivalent to m = m*5
console.log(m);
m %= 5; // Equivalent to m = m % 5 
console.log(m);
m **= 5; // Equivalent to m = m ** 5 
console.log(m);