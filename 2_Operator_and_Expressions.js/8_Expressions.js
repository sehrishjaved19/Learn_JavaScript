// Arithmetic Expressions
let sum = 5+3;//combining numbers with operators
console.log(sum);

// Variable Expressions
let x = 10;
let result = x*2; //combining variable with operator
console.log(result);

//Function Call Expressions
function square(number){
    return number*number;
}
let squaredValue = square(4);//here squaredValue stores the return value of the function square of argument 4.
console.log(squaredValue);

//Conditional Expressions (Ternary operator)
let age = 20;
let canVote = age >=18 ? "yes" : "no";
console.log(canVote);
